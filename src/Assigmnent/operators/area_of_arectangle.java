package Assigmnent.operators;

public class area_of_arectangle {
    public static void main(String[] args) {
        // Define the sides of the rectangle
        double length = 2.0;
        double width = 3.0;

        // Calculate the area of the rectangle
        double area = length * width;

        // Print the area of the rectangle
        System.out.println("The area of the rectangle with sides "  + length + "  units and " + width + "  units is: "  );
        System.out.println(area + " square units");
    }
}
