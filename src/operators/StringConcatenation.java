package operators;

public class StringConcatenation {
    public static void main(String[] args) {
        System.out.println(10+23);//addition
        System.out.println(10 + 'A');//addition
        System.out.println('A'+'A');// addition
        System.out.println('A'+"A"+12+12);
        System.out.println('A'+("A"+12)+2);


    }
}
